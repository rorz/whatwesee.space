const MAX_QUERY_LEN = 9;
const BG = "#0d1b2e";
const STRIP_BG = "#091624";
const MATCH_BG = "#2a1800";
const MATCH_FG = "#f5a623";
const ADDRESS_FG = "#f5a623";
const LABEL_FG = "#3a6a94";
const QUERY_FG = "#e8e8e8";
const NOT_FOUND_FG = "#4a2a10";

function searchPi(query: string): number {
  if (!query) return -1;
  return PI_DIGITS.indexOf(query);
}

export default function TheAddress() {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const inputRef = useRef<HTMLInputElement>(null);
  const stateRef = useRef({
    query: "",
    matchPos: -1,
    scrollRow: 0,
    targetRow: 0,
  });

  useEffect(() => {
    const canvas = canvasRef.current;
    const input = inputRef.current;
    if (!canvas || !input) return;
    const ctx = canvas.getContext("2d", { alpha: false });
    if (!ctx) return;

    let rafId = 0;
    let resizeObserver: ResizeObserver | null = null;

    const layout = {
      size: 640,
      topH: 80,
      gridY: 80,
      gridH: 464,
      gridRows: 25,
      stripY: 544,
      stripH: 96,
      cols: 46,
      charW: 14,
      charH: 18,
    };

    const fitCanvas = () => {
      const rect = canvas.getBoundingClientRect();
      const dpr = Math.min(window.devicePixelRatio || 1, 2);
      const size = Math.floor(Math.min(rect.width, rect.height));
      layout.size = size;
      canvas.width = Math.max(1, Math.floor(size * dpr));
      canvas.height = Math.max(1, Math.floor(size * dpr));
      ctx.setTransform(dpr, 0, 0, dpr, 0, 0);

      layout.cols = size >= 480 ? 46 : size >= 360 ? 32 : 22;
      layout.topH = Math.round(size * 0.13);
      layout.stripH = Math.round(size * 0.16);
      layout.gridY = layout.topH;
      layout.gridH = size - layout.topH - layout.stripH;
      layout.charH = Math.round(layout.gridH / Math.floor(layout.gridH / (size >= 480 ? 18 : 14)));
      layout.gridRows = Math.floor(layout.gridH / layout.charH);
      layout.charW = Math.floor(size / layout.cols);
      layout.stripY = layout.topH + layout.gridH;
    };

    resizeObserver = new ResizeObserver(fitCanvas);
    resizeObserver.observe(canvas);
    fitCanvas();

    const render = () => {
      const { size, topH, gridY, gridH, gridRows, stripY, stripH, cols, charW, charH } = layout;
      const { query, matchPos, scrollRow } = stateRef.current;

      ctx.fillStyle = BG;
      ctx.fillRect(0, 0, size, size);

      // ── address header ──────────────────────────────────────────
      const addrFontSize = Math.round(topH * 0.44);
      ctx.font = `400 ${addrFontSize}px 'Geist Mono', 'Courier New', monospace`;
      ctx.textAlign = "left";
      const addrX = Math.round(charW * 0.6);
      const addrY = Math.round(topH * 0.68);

      if (query.length === 0) {
        ctx.fillStyle = LABEL_FG;
        ctx.fillText("π  DIRECTORY", addrX, addrY);
      } else if (matchPos >= 0) {
        ctx.fillStyle = ADDRESS_FG;
        ctx.fillText(`π[ ${matchPos.toLocaleString()} ]`, addrX, addrY);
      } else {
        ctx.fillStyle = NOT_FOUND_FG;
        ctx.fillText("π[ beyond 50 000 ]", addrX, addrY);
      }

      // ── digit grid ──────────────────────────────────────────────
      ctx.save();
      ctx.beginPath();
      ctx.rect(0, gridY, size, gridH);
      ctx.clip();

      const digitFontSize = Math.round(charH * 0.68);
      ctx.font = `400 ${digitFontSize}px 'Geist Mono', 'Courier New', monospace`;
      ctx.textAlign = "center";

      const startRow = Math.floor(scrollRow);
      const subPx = (scrollRow - startRow) * charH;

      for (let row = 0; row <= gridRows; row++) {
        const actualRow = startRow + row;
        const y = gridY + row * charH - subPx + Math.round(charH * 0.76);

        for (let col = 0; col < cols; col++) {
          const idx = actualRow * cols + col;
          if (idx >= PI_DIGITS.length) break;
          const digit = PI_DIGITS[idx];
          const x = charW * col + Math.round(charW * 0.5);

          const isMatch =
            matchPos >= 0 && query.length > 0 && idx >= matchPos && idx < matchPos + query.length;

          if (isMatch) {
            ctx.fillStyle = MATCH_BG;
            ctx.fillRect(charW * col, y - Math.round(charH * 0.8), charW, charH);
            ctx.fillStyle = MATCH_FG;
          } else {
            const d = parseInt(digit, 10);
            const alpha = 0.12 + (d / 9) * 0.22;
            ctx.fillStyle = `rgba(90,160,210,${alpha.toFixed(2)})`;
          }

          ctx.fillText(digit, x, y);
        }
      }

      ctx.restore();

      // ── search strip ─────────────────────────────────────────────
      ctx.fillStyle = STRIP_BG;
      ctx.fillRect(0, stripY, size, stripH);
      ctx.fillStyle = "#0f2438";
      ctx.fillRect(0, stripY, size, 1);

      const labelFontSize = Math.round(stripH * 0.26);
      ctx.font = `400 ${labelFontSize}px 'Geist Mono', 'Courier New', monospace`;
      ctx.textAlign = "left";
      ctx.fillStyle = LABEL_FG;
      ctx.fillText("search →", Math.round(size * 0.04), stripY + Math.round(stripH * 0.38));

      const queryFontSize = Math.round(stripH * 0.46);
      ctx.font = `400 ${queryFontSize}px 'Geist Mono', 'Courier New', monospace`;

      const caret = Date.now() % 900 < 450 ? "▌" : " ";
      const displayQuery = query.length > 0 ? query + caret : caret;
      ctx.fillStyle = QUERY_FG;
      ctx.fillText(displayQuery, Math.round(size * 0.04), stripY + Math.round(stripH * 0.82));
    };

    const loop = () => {
      const s = stateRef.current;

      // Slow auto-scroll when idle
      if (s.query.length === 0) {
        s.targetRow += 0.012;
        const maxRow = Math.max(0, Math.floor(PI_DIGITS.length / layout.cols) - layout.gridRows);
        if (s.targetRow > maxRow) s.targetRow = 0;
      }

      // Smooth approach to target
      const diff = s.targetRow - s.scrollRow;
      s.scrollRow += Math.abs(diff) > 0.01 ? diff * 0.09 : diff;

      render();
      rafId = requestAnimationFrame(loop);
    };

    const handleInput = () => {
      const raw = input.value.replace(/\D/g, "").slice(0, MAX_QUERY_LEN);
      input.value = raw;
      const s = stateRef.current;
      s.query = raw;

      if (raw.length > 0) {
        const pos = searchPi(raw);
        s.matchPos = pos;
        if (pos >= 0) {
          const matchRow = Math.floor(pos / layout.cols);
          s.targetRow = Math.max(0, matchRow - Math.floor(layout.gridRows / 2));
          s.scrollRow = s.targetRow;
        }
      } else {
        s.matchPos = -1;
        s.targetRow = 0;
      }
    };

    const focusInput = () => input.focus();

    canvas.addEventListener("click", focusInput);
    canvas.addEventListener("touchstart", focusInput);
    input.addEventListener("input", handleInput);

    // Mobile-friendly: also listen for keydown on document when canvas is active
    const handleKeyDown = (e: KeyboardEvent) => {
      if ((e.key >= "0" && e.key <= "9") || e.key === "Backspace") {
        input.focus();
      }
    };
    document.addEventListener("keydown", handleKeyDown);

    input.focus();

    window.the_address_render_to_text = () => {
      const { query, matchPos, scrollRow } = stateRef.current;
      return `The Address | query="${query}" | address=${matchPos >= 0 ? `π[${matchPos}]` : "not-found"} | row=${Math.round(scrollRow)}`;
    };

    window.the_address_advance = (steps: number) => {
      stateRef.current.targetRow = Math.max(0, stateRef.current.targetRow + steps);
      stateRef.current.scrollRow = stateRef.current.targetRow;
    };

    rafId = requestAnimationFrame(loop);

    return () => {
      cancelAnimationFrame(rafId);
      resizeObserver?.disconnect();
      canvas.removeEventListener("click", focusInput);
      canvas.removeEventListener("touchstart", focusInput);
      input.removeEventListener("input", handleInput);
      document.removeEventListener("keydown", handleKeyDown);
      delete window.the_address_render_to_text;
      delete window.the_address_advance;
    };
  }, []);

  return (
    <div className="relative h-full w-full" style={{ background: BG }}>
      <canvas
        ref={canvasRef}
        className="block h-full w-full cursor-text select-none"
        aria-label="π directory — type digit sequences to find their address in pi. The grid scrolls to reveal where your numbers live."
      />
      <input
        ref={inputRef}
        type="tel"
        maxLength={MAX_QUERY_LEN}
        className="pointer-events-none absolute opacity-0"
        style={{ top: "84%", left: "4%", width: "1px", height: "1px" }}
        tabIndex={-1}
        aria-hidden="true"
        defaultValue=""
      />
    </div>
  );
}
